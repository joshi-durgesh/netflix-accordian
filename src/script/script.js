function showContent1() {
  const content = document.getElementById("accordian-content-1");
  const iconAnime = document.getElementById("btn-icon-1");
  if (content.style.display == "none") {
    content.style.display = "block";
    iconAnime.style.transform = "rotateZ(45deg)";
  } else {
    iconAnime.style.transform = "rotateZ(0deg)";
    content.style.display = "none";
  }
}

